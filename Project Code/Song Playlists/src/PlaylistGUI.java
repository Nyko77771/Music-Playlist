    private void btnAddLikedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddLikedActionPerformed
        // TODO add your handling code here:
        getLikedData();
        if(songName.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Enter Song Name");
        } else if (albumName.isEmpty()) {
            song = new Song(songName,artistName);
        } else {
            song = new Song(songName, artistName, albumName);
        }
        if(!(songName.isEmpty())){
            likedSongs.push(song);
            lblPostMessages.setText("Song Added to Liked");
        }
    }//GEN-LAST:event_btnAddLikedActionPerformed

