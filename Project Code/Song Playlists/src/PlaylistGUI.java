    private void btnRemoveLiked2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveLiked2ActionPerformed
        // TODO add your handling code here:
        try{
            int removeIndex = Integer.parseInt(lblRemoveLiked2.getText());
            Song removedSong = generalPlaylist2.get(removeIndex);
            generalPlaylist2.remove(removeIndex);
            if(removedSong == null){
                lblPostMessages.setText("Nothing to remove");
            } else {
                lblPostMessages.setText("Removed " + removedSong);
            }
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a number");
        }
    }//GEN-LAST:event_btnRemoveLiked2ActionPerformed

